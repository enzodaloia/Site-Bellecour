<h1> Story </h1>
<article>
    <p>
    In Gallo-Roman times, the Bellecour district was an island or a peninsula formed of alluvial land.<br>
    At that time, the district experienced Roman, military and commercial activity: <br>
    canabae, barracks that served as warehouses for Gallo-Roman merchants and nautes, have been found there6,<br>
<br>
the city was then located on the Fourvière hill, before it descended to the bank of the Saône in the 3rd century.<br>
At the end of the 12th century, the archbishop of Lyon owned the "bella curtis" on the peninsula, which consisted of land where a vineyard grew.<br>
Abandoned, the place becomes marshy again.<br>
In 1562, the Baron des Adrets, attacking Lyon, installed his men-at-arms on what was then the "Pré de Belle-Court".<br>
In 1604, Henry IV urged the city council to acquire the meadow in order to create a public square. <br>
But the archbishop's heirs argue in an interminable trial.<br>
<br>
On 2 December 1614, the Aumône Générale bought land between the Place Bellecour and the Rhône to build a hospital.<br>
The Hospital de la Charité in Lyon was built from 1617 to 1622. The hospital's church opens onto the Place Bellecour.<br>
The Place de la Charité is located between the south-east corner of the Place Bellecour and the quays of the Rhône, along the north side of the hospital.<br>
In 1708, Louis XIV obtained final possession of the square. <br>
The Place Louis-le-Grand was adorned with a bronze statue of the king by Martin Desjardins.<br>
The buildings around the square were designed by the king's first architect, Robert de Cotte.<br>
Louis XIV visited Lyon only once, in his youth, in 1658. Since the installation in Versailles,<br>
the king is present in the provinces mainly through his equestrian statues.
À la Révolution, un autel de la Liberté y est élevé le 14 juillet 1790. On y installe une guillotine en 1792. La statue royale est détruite en 1793.<br>
On 21 June 1800, Bonaparte, passing through Lyon after the victory at Marengo, laid the first stone of the new buildings.<br>
During the Restoration, the square was adorned with a new statue of Louis XIV, by the sculptor François-Frédéric Lemot, inaugurated on 6 November 1825.<br>
In 1829, Madame Girard set up a renowned bar on the square.<be>
The square is indirectly concerned by the transformation works of the peninsula led by the prefect Vaïsse during the Second Empire.<br>
Some buildings were demolished to the north of the square to allow the opening of new roads from the Place Bellecour.<br>
<br>
      <li> widening of Rue Bellecordière, incorporated into the new Rue Impériale (Rue de la République) towards Place de la Comédie  </li>
      <li> creation of the rue de l'Impératrice (rue Édouard-Herriot) towards the Place des Terreaux </li>
      <li> widening of the Place Le Viste at the junction of the two previous streets </li>
      <li>creation of rue Gasparin on the site of part of the former prefecture (former Jacobin convent)</li>
<br>
The two pavilions on the square were built in 1852 by Tony Desjardins. <br>
The one to the west, next to the kindergartens, was, before it became the offices of the tourist office "Le Rectangle", <br>
an exhibition hall called Maison de Lyon. It was originally a café, La gargote du Père Lathuille, before becoming a dance café-restaurant called La Maison Dorée.<br>
The building to the south-east of the square, in which the tourist office was installed in 2008,<br>
was a military pavilion, later transformed into a police station, then into a tourist office in 1924.<br>
In 1934, the Charité hospital was destroyed, with the exception of the church tower. The Place de la Charité was then enlarged and became the current Place Antonin-Poncet.<br>
<br>
Since 2011, major beautification work has been taking place on the southern part of the square. The programme includes: 
      <li> moving the two water basins </li>
      <li> renovation of the five kiosks on the east side and construction of a new kiosk on the west side</li>
      <li> renovation of the children's playground, new lighting (LED street lamps)</li>
      <li> planting hedges and trees (replacing chestnut trees with lime and cherry trees)</li>
<br>
It should be noted that the lime tree was the first species planted on the square in the 17th century.
</p>
