<h1>Odonymy</h1>
<article>
<p> The toponym from which the word "Bellecour" is derived appeared as early as the 11th century.<br>
 The agricultural land owned by the archbishop was called bella curtis ("beautiful garden"). <br>
 In the 16th century, what was still only a pasture was called the Belle Court meadow4. In 1715, it became a public square, <br>
 it was named Place Royale, then Place Louis-le-Grand in homage to Louis XIV.</p>
<br> In 1790, during the Revolution, the monarchist names disappeared and the square became the Place de la Fédération,<br>
 to perpetuate the memory of the Fête de la Fédération, whose anniversary has since marked the French bank holidays.<br>
 From 1793 onwards, the place for political demonstrations in the city was renamed Place de l'Égalité (in 1794, Place des Jacobins was called Place de la Fraternité). <br>
 The odonym continues to evolve with political regimes: 
 in 1800 the esplanade is the Place Bonaparte and logically becomes a little later Place Napoléon <br>
 (before, under the Second Empire, it was the Place Carnot which was given this name).</br>
<br> In 1814, under the Restoration, the square was renamed Place Louis-le-Grand. It was not until the Third Republic that the square took on its current name of Place Bellecour. <br>
It was renamed Place du Maréchal-Pétain after a visit by the head of the Vichy regime to Lyon on 18 and 19 November 19405. It was renamed Place Bellecour after the Liberation.</br>
</article>
