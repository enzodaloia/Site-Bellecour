<h1> Histoire </h1>
<article>
    <p>
    À l’époque gallo-romaine, le quartier de Bellecour est une île ou une presqu'île formée de terres alluviales.<br>
    Le quartier connait alors une activité romaine, militaire et marchande : <br>
    on y a retrouvé des canabae, baraques qui servaient d’entrepôt aux négociants et aux nautes gallo-romains6,<br>
<br>
    la ville se situant alors sur la colline de Fourvière, avant qu'elle en descende en rive de la Saône au IIIe.<br>
    À la fin du XIIe, l'archevêque de Lyon possède sur la presqu'île la bella curtis, composée de terrains où pousse notamment une vigne. <br>
    Abandonné, l'endroit redevient marécageux.<br>
    En 1562, le baron des Adrets attaquant Lyon, installe ses hommes d'arme sur ce qui est alors « le pré de Belle-court »4.<br>
    En 1604, Henri IV pousse le Conseil de la ville à acquérir le pré afin d'y aménager une place publique. <br>
    Mais les héritiers de l'archevêque se disputent lors d'un interminable procès.<br>
<br>
    L'Aumône générale achète le 2 décembre 1614 des terrains situés entre la place Bellecour et le Rhône afin d'y construire un hôpital.<br>
    L'hôpital de la Charité de Lyon est construit de 1617 à 1622. L'église de l'hôpital s'ouvre sur la place Bellecour. <br>
    La place de la Charité est aménagée entre l'angle sud-est de la place Bellecour et les quais du Rhône, le long du flanc nord de l'hôpital.<br>
    En 1708, Louis XIV obtient la possession définitive de la place. <br>
    La place Louis-le-Grand est alors ornée d'une statue de bronze représentant le roi et réalisée par Martin Desjardins.<br>
    Autour de la place sont construits des édifices, dont les façades sont dessinées par le premier architecte du roi Robert de Cotte.<br>
    Louis XIV visite Lyon une seule fois, dans sa jeunesse, en 1658. Depuis l'installation à Versailles,<br>
    le roi est présent en province surtout par ses statues équestres.<br>
    À la Révolution, un autel de la Liberté y est élevé le 14 juillet 1790. On y installe une guillotine en 1792. La statue royale est détruite en 1793.<br>
    Le 21 juin 1800, Bonaparte de passage à Lyon après la victoire de Marengo, pose la première pierre des nouveaux édifices.<br>
    Lors de la Restauration, la place s'orne d'une nouvelle statue de Louis XIV, due au sculpteur François-Frédéric Lemot, inaugurée le 6 novembre 1825.<br>
    En 1829, Madame Girard installe une buvette de renommée sur la place.<br>
    La place est indirectement concernée par les travaux de transformation de la presqu'île menés par le préfet Vaïsse pendant le Second empire.<br>
    Quelques immeubles sont démolis au nord de la place pour permettre l'ouverture de nouvelles voies à partir de la place Bellecour : <br>
<br>
      <li> élargissement de la rue Bellecordière, incorporée à la nouvelle rue Impériale (rue de la République) vers la place de la Comédie  </li>
      <li> création de la rue de l'Impératrice (rue Édouard-Herriot) vers la place des Terreaux </li>
      <li> élargissement de la place Le Viste à la jonction des deux précédentes rues </li>
      <li>création de la rue Gasparin à l'emplacement d'une partie de l'ancienne préfecture (ancien couvent des Jacobins</li>
<br>
 Les deux pavillons situés sur la place ont été construits en 1852 par Tony Desjardins. <br>
 Celui qui est à l'ouest, à côté des jardins d'enfants, était, avant de devenir les bureaux de l'office de tourisme « Le Rectangle », <br>
 une salle d'exposition nommée Maison de Lyon. C'était initialement un café, La gargote du Père Lathuille, avant de devenir un café-restaurant dansant nommé La Maison Dorée.<br>
 Le bâtiment situé au sud-est de la place, dans lequel a été installé l'office du tourisme en 2008,
  était un pavillon militaire, plus tard transformé en poste de police, puis en Syndicat d'Initiative en 1924.<br>
 En 1934, l'hôpital de la Charité est détruit, à l'exception du clocher de l'église. La place de la Charité est alors agrandie et devient l'actuelle place Antonin-Poncet.<br>
<br>
Depuis 2011, d'importants travaux d'embellissement ont lieu sur la partie sud de la place. Au programme : 
      <li> déplacements des deux bassins d'eau </li>
      <li> rénovation des cinq kiosques sur le côté est et construction d'un nouveau kiosque sur le côté ouest</li>
      <li> rénovation de l'aire de jeux pour enfants, nouvel éclairage (lampadaires à LEDs)</li>
      <li> plantation de haies et d'arbres (remplacement des marronniers par des tilleuls et des merisiers)</li>
<br>
Il est à noter que le tilleul est la première essence plantée sur la place au xviie siècle.
</p>
